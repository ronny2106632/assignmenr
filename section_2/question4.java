
public class MyClass {
  
  // non-static method
  public void myMethod() {
    System.out.println("I am a non-static method");
  }
  
  // static method 1
  public static void myStaticMethod1() {
    System.out.println("I am static method 1");
  }
  
  // static method 2
  public static void myStaticMethod2() {
    System.out.println("I am static method 2");
  }
  
  // constructor
  public MyClass() {
    System.out.println("I am a constructor");
  }
  
}